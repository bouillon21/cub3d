#include "libft.h"

int	ft_atoi(const char *s)
{
	int		i;
	int		minus;
	long	rez;

	i = 0;
	minus = 1;
	rez = 0;
	while (s[i] == '\t' || s[i] == '\n' || s[i] == '\v'
			|| s[i] == '\f' || s[i] == '\r' || s[i] == ' ')
		i++;
	if (s[i] == '-' && (s[i + 1] >= 48 && s[i + 1] <= 58))
	{
		minus = -1;
		i++;
	}
	else if (s[i] == '+')
		i++;
	while (s[i] > 47 && s[i] < 58)
	{
		rez = rez * 10 + (s[i] - 48);
		i++;
	}
	return (rez * minus);
}
